//conditional operator
let x = 10;
let y = "10";

if(x == y){
    console.log("equal");
}else{
    console.log("not equal");
}

if(x === y){
    console.log("equal");
}else{
    console.log("not equal");
}

if(x != y){
    console.log("true");
}else{
    console.log("false");
}

if(x !== y){
    console.log("true");
}else{
    console.log("false");
}